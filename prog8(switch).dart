void main() {
  String mobile = 's';
  switch (mobile) {
    case 's':
      print('Samsung');
      break;

    case 're':
      print('Redmi');
      break;

    case 'r':
      print('Realme');
      break;

    case 'a':
      print('Apple');
      break;

    default:
      print('Inalid Selection');
  }
}
